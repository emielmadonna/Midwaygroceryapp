const DEFAULT_SITE_WIDTH = 78;
const DEFAULT_SITE_HEIGHT = 34;

function normalizeX(value) {
  return Number((value / RV_MAP_COORDINATE_SYSTEM.sourceViewBox.width).toFixed(4));
}

function normalizeY(value) {
  return Number((value / RV_MAP_COORDINATE_SYSTEM.sourceViewBox.height).toFixed(4));
}

function createRvSite({
  siteNumber,
  x,
  y,
  width = DEFAULT_SITE_WIDTH,
  height = DEFAULT_SITE_HEIGHT,
  rotation = 0,
  amp,
  hookup = 'full',
  type,
  shade,
  amenities,
  customerNotes,
  adminNotes,
}) {
  const isFullHookup = hookup === 'full';

  return Object.freeze({
    id: `rv-${siteNumber}`,
    siteNumber,
    displayName: `Site ${siteNumber}`,
    status: siteNumber === '15' ? 'inactive' : 'active',
    nightlyPriceCents: isFullHookup ? 4500 : 4000,
    maxRvLengthFeet: isFullHookup ? 40 : 30,
    mapX: normalizeX(x),
    mapY: normalizeY(y),
    mapWidth: normalizeX(width),
    mapHeight: normalizeY(height),
    rotation,
    amp,
    hookup,
    type,
    shade,
    sku: `MIDWAY-RV-${siteNumber}-${amp.replace('A', 'AMP')}`,
    amenities: Object.freeze(amenities),
    customerNotes,
    adminNotes,
  });
}

function createTentSite({
  siteNumber,
  x,
  y,
  width = 48,
  height = 30,
  rotation = 0,
}) {
  return Object.freeze({
    id: `tent-${siteNumber}`,
    siteNumber: `T${siteNumber}`,
    displayName: `Tent ${siteNumber}`,
    status: 'active',
    nightlyPriceCents: 2000,
    maxRvLengthFeet: 0,
    mapX: normalizeX(x),
    mapY: normalizeY(y),
    mapWidth: normalizeX(width),
    mapHeight: normalizeY(height),
    rotation,
    amp: 'Tent',
    type: 'tent',
    shade: 'partial',
    sku: `MIDWAY-TENT-${siteNumber}`,
    amenities: Object.freeze([
      RV_SITE_AMENITIES.TENT_AREA,
      RV_SITE_AMENITIES.WALK_IN,
      RV_SITE_AMENITIES.PICNIC_TABLE,
      RV_SITE_AMENITIES.WALK_TO_STORE,
    ]),
    customerNotes: 'Walk-in tent area on the center island with easy access to the store.',
    adminNotes: 'Center island tent inventory; confirm final site boundaries before peak-season launch.',
  });
}

