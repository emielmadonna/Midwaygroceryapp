export function denormalizeMapSite(site, viewBox = RV_MAP_COORDINATE_SYSTEM.sourceViewBox) {
  return {
    ...site,
    mapX: Math.round(site.mapX * viewBox.width),
    mapY: Math.round(site.mapY * viewBox.height),
    mapWidth: Math.round(site.mapWidth * viewBox.width),
    mapHeight: Math.round(site.mapHeight * viewBox.height),
  };
}

export function getRvMapSiteByNumber(siteNumber) {
  const normalizedSiteNumber = String(siteNumber).padStart(2, '0');
  return rvMapSites.find(site => site.siteNumber === normalizedSiteNumber) ?? null;
}
